package com.example.Switch;

public class App {

    public static void main(String[] args) {
        int codigoProduto = 2;
        
        switch(codigoProduto) {
            case 1:
                System.out.println("Cadastrar");
                break;
            case 2:
                System.out.println("Pesquisar");
                break;
            default:
                System.out.println("Escolha um valor valido");
                
    }
}
}
